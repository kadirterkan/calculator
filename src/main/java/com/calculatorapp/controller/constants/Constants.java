package com.calculatorapp.controller.constants;

public class Constants {

    public static final double buttonSize = 75;
    public static final double zeroWidth = 150;
    public static final double labelHeight = 100;
    public static final double labelWidth = buttonSize * 4;

    public double getButtonSize() {
        return buttonSize;
    }

    public double getZeroWidth() {
        return zeroWidth;
    }

    public double getLabelHeight() {
        return labelHeight;
    }

    public double getLabelWidth() {
        return labelWidth;
    }
}
